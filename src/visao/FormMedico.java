    public void preencherTabela(String sql){
        ArrayList dados = new ArrayList();
        String[] colunas = new String[] {"ID","Nome","Nascimento","Nº Conselho"}; 
        
        conex.conectarPostgre();
        conex.executarSql(sql);
        try{
           conex.rs.first();
           
           do{
               dados.add(new Object[]{conex.rs.getInt("codigo_medico"),conex.rs.getString("nome_medico"),conex.rs.getString("data_nasc"),conex.rs.getInt("n_conselho")});
           }while(conex.rs.next());
           
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(rootPane, "Busque por outro Médico para preencher a tabela! " );
        }
        ModeloTabela modelomedico = new ModeloTabela(dados, colunas); //Passar a qnt de linhas e colunas
        
        jTableListarMedCadastrados.setModel(modelomedico);
        jTableListarMedCadastrados.getColumnModel().getColumn(0).setPreferredWidth(2);
        jTableListarMedCadastrados.getColumnModel().getColumn(0).getResizable();
        jTableListarMedCadastrados.getColumnModel().getColumn(1).setPreferredWidth(180);
        jTableListarMedCadastrados.getColumnModel().getColumn(1).getResizable();
        jTableListarMedCadastrados.getColumnModel().getColumn(2).setPreferredWidth(80);
        jTableListarMedCadastrados.getColumnModel().getColumn(2).getResizable();
        jTableListarMedCadastrados.getColumnModel().getColumn(3).setPreferredWidth(80);
        jTableListarMedCadastrados.getColumnModel().getColumn(3).getResizable();

        jTableListarMedCadastrados.getTableHeader().setReorderingAllowed(false);
        //jTableListarMedCadastrados.setAutoResizeMode(jTableListarMedCadastrados.AUTO_RESIZE_OFF);
        jTableListarMedCadastrados.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        
        conex.desconectar();
    }
    
